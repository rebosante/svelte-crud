                <h3 style="font-weight: 500; margin-top: 0; color: #1f1c3f; font-size: 24px;">Release notes</h3>
                <h4 style="color: #495588; font-size: 16px;">React InstantSearch v5.0.0</h4>
                <p style="line-height: 22px; margin-bottom: 32px; color: #495588; font-size: 16px;">
                  Following our beta, the final version of React InstantSearch 5.0.0 is out. Thank you for your feedback! It includes:
                </p>
                <ul>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;">Complete revamp of the HTML output of most widgets. The goal of this release is to provide improved semantics to our users.</p>
                  </li>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;">A new CSS naming convention which will be reused across all InstantSearch libraries. This will enable the possibility to develop cross-library CSS themes easily.</p>
                  </li>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;">Alignment on naming for widgets names & attributes.</p>
                  </li>
                </ul>
                <p style="line-height: 22px; color: #495588; font-size: 16px;">You can find the full migration guide from v4 to v5 in <a href="https://goto.algolia.com/e/139121/-guide-Migration-guide-v5-html/29bqhm/272951473" class="text-algolia-link" style="color: #00aeff; text-decoration: none;">our documentation</a>. GitHub repo <a href="https://goto.algolia.com/e/139121/algolia-react-instantsearch/29bqhp/272951473" class="text-algolia-link" style="color: #00aeff; text-decoration: none;">here</a>.</p>
                <h4 style="color: #495588; font-size: 16px;">InstantSearch.js 2.6.0</h4>
                <p style="line-height: 22px; margin-bottom: 32px; color: #495588; font-size: 16px;">
                  A lot of new features and improvements this month for our InstantSearch.js library:
                </p>
                <ul>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;">a new widget to display results on a map (with Google Maps or any provider)</p>
                  </li>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;">a widget to configure your search settings</p>
                  </li>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;">support for <a href="https://github.com/algolia/search-insights.js" class="text-algolia-link" style="color: #00aeff; text-decoration: none;">Algolia Search Insights</a> (click analytics)</p>
                  </li>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;">the reworked pagination connector now embeds the logic for the padding of pages</p>
                  </li>
                </ul>
                <p style="line-height: 22px; color: #495588; font-size: 16px;">Find more on our <a href="https://goto.algolia.com/e/139121/-guide-Migration-guide-v5-html/29bqhm/272951473" class="text-algolia-link" style="color: #00aeff; text-decoration: none;">community forum</a>.</p>
                <h4 style="color: #495588; font-size: 16px;">JavaScript API client v3.25.0</h4>
                <p style="line-height: 22px; margin-bottom: 32px; color: #495588; font-size: 16px;">
                  To avoid confusion on pages and improve performance on old devices, our JavaScript API client will now use https by default.
                  Follow the <a href="https://goto.algolia.com/e/139121/lgoliasearch-client-javascript/29bqht/272951473" class="text-algolia-link" style="color: #00aeff; text-decoration: none;">GitHub repo</a>.
                </p>
                <h4 style="color: #495588; font-size: 16px;">Vue InstantSearch 1.5.2</h4>
                <p style="line-height: 22px; margin-bottom: 32px; color: #495588; font-size: 16px;">
                  The last Vue InstantSearch version fixes small issues, and includes two fixes submitted by community members!
                  Check out the changelog <a href="https://goto.algolia.com/e/139121/earch-blob-master-CHANGELOG-md/29bqhw/272951473" class="text-algolia-link" style="color: #00aeff; text-decoration: none;">here</a>, and participate in the library improvements on <a href="https://goto.algolia.com/e/139121/algolia-vue-instantsearch/29bqhy/272951473" class="text-algolia-link" style="color: #00aeff; text-decoration: none;">GitHub</a>.
