                <h3 style="font-weight: 500; margin-top: 0; color: #1f1c3f; font-size: 24px;">Algolia for Zendesk</h3>
                <p style="line-height: 22px; margin-bottom: 32px; color: #495588; font-size: 16px;">
                  A little under two years ago, we released an extension for Zendesk, our first integration with a Help Center solution. Last week, we were proud to have become an official Zendesk Guide and Zendesk Guide Enterprise partner. Here is what Algolia search brings to your Zendesk Help Center:
                </p>
                <ul>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;"><strong>Federated search</strong>: break down content silos and empower your customers to find answers across all of your content.</p>
                  </li>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;"><strong>Real-time search</strong>: direct customers to the right content with results that update with each keystroke, regardless of typos.</p>
                  </li>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;"><strong>Analytics & insights</strong>: understand what drives your customers' search requests, and prioritize new Help Center content based on zero result search queries.</p>
                  </li>
                  <li>
                    <p style="line-height: 22px; color: #495588; font-size: 16px;"><strong>Customizability</strong>: our code is open source. Fork it, make it your own and design the experience that best fits your brand.</p>
                  </li>
                </ul>
                <p style="line-height: 22px; color: #495588; font-size: 16px;">Read the <a href="https://goto.algolia.com/e/139121/zendesk-guide-search-/29bqhh/272951473" class="text-algolia-link" style="color: #00aeff; text-decoration: none;">blog post</a> about the release, or just <a href="https://goto.algolia.com/e/139121/integrations-zendesk/29bqhk/272951473" class="text-algolia-link" style="color: #00aeff; text-decoration: none;">get started</a>!</p>
